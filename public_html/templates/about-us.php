                <div class="col-sm-12">
                    <p class="small-title mb-30">IEEE, The Institue of Electrical and Electronics Engineers yani Elektrik ve Elektronik Mühendisleri Enstitüsü, başta elektrik, elektronik, bilgisayar ve telekomünikasyon alanlarında olmak üzere birçok alanda mühendislik teorileri ve uygulamalarını geliştirmeye çalışan ve kar amacı gütmeyen bir kuruluştur. Aynı zamanda dünyanın önde gelen sayılı teknik organizasyonlarındandır. Bu organizasyonun temelleri 19. Yüzyılda Graham Bell ve Thomas Edison gibi dönemin önemli bilim adamlara tarafından atılmıştır.</p>
                    <p class="small-title mb-30">IEEE, Türkiye’nin de içinde bulunduğu 150 ülkede 400.000’den fazla üyeyle faaliyet göstermektedir. 400.000 kadar üyenin 70.000’den fazlası öğrencidir. Türkiye’de bu organizasyona dâhil olan kişi sayısı 25.000’dir ve bunların 5000’den fazlası öğrencidir. Bu organizasyon dünyada elektrik, elektronik, bilgisayar ve otomasyon mühendisliklerinin yayınladıkları yayınların yaklaşık %30’unu yayınlar.</p>
                    <p class="small-title mb-30">Günümüzde internet bağlantılarında kullanılan wireless (kablosuz) ve ethernet (kablolu) bağlantı türlerini ve kablosuz internetlerde sıklıkla karşılaştığımız “802” ile başlayan kodların geliştiricileri de yine IEEE organizasyonudur.</p>
                    <p class="small-title mb-30">Merkezi ABD’nin New Jersey eyaletinde olan kuruluşun temel amaçları teknolojiyi geliştirmeye yönelik teşviklerde bulunmak ama teknoloji sayesinde dünya çapında toplulukların ilerleyebilmesini sağlamaktır. Ayrıca IEEE üyeleri hem bu alanda kendini geliştirebilir hem de ürettikleri projeleri dünyaya daha kolay sunma fırsatına sahip olabilirler. IEEE bunları yapabilmek için dünya genelinde çeşitli yayınlar yapar ve üniversitelerdeki IEEE öğrenci kolları aracılığı ile yine üniversitelerde konferanslar toplantılar düzenler. Her yıl yüzlerce uluslar arası konferans ve binlerce toplantı düzenlenir. Mesleki ve teknik standartlar belirler ve bu standartları IEEE ile ifade ederi. Eğer başında IEEE olan bir teknik terim gördüyseniz bilin ki o terim IEEE tarafından belirlenmiştir. Özellikle bilişim alanında birçok standart IEEE standartıdır. Hatta yine bilişim ve elektrik elektronik ile ilgili olan bir çok standart kuruluşta IEEE standartları üzerine kurulmaktadır.</p>
